
ALTER SESSION SET "_ORACLE_SCRIPT"=TRUE;

CREATE USER MUSIC
IDENTIFIED BY TOWN
DEFAULT TABLESPACE USERS QUOTA UNLIMITED ON USERS;

GRANT CREATE SESSION, CREATE TABLE, CREATE SEQUENCE TO MUSIC;

------------------------------------------------------------------

CREATE TABLE "MUSIC"."MUSIC"
(	"MUSIC_NO" NUMBER(8,0) PRIMARY KEY, 
	"TITLE" VARCHAR2(200 BYTE) NOT NULL, 
	"VOCAL" VARCHAR2(200 BYTE) NOT NULL,
	"CREATE_DATE" DATE DEFAULT SYSDATE
);

CREATE TABLE "MUSIC"."MUSIC_DETAIL" 
(	"MUSIC_DETAIL_NO" NUMBER(8,0) PRIMARY KEY, 
	"RELEASE_COMP" VARCHAR2(200 BYTE) NOT NULL, 
	"RELEASE_DATE" DATE DEFAULT SYSDATE,
	"MUSIC_NO" NUMBER(8,0),	
	"CREATE_DATE" DATE DEFAULT SYSDATE
);

CREATE SEQUENCE  "MUSIC"."SEQ_MUSIC"  
MINVALUE 0 
MAXVALUE 9999 
INCREMENT BY 1 
START WITH 1 
CACHE 2 
NOORDER  
NOCYCLE  
NOKEEP  
NOSCALE  
GLOBAL;

